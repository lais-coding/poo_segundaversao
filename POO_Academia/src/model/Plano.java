package model;

public class Plano {

	private String tituloPlano;
	private double valorMensal;
	private double valorAnual;
	private String beneficios;
	
	
	public String getTituloPlano() {
		return tituloPlano;
	}
	public void setTituloPlano(String tituloPlano) {
		this.tituloPlano = tituloPlano;
	}
	public double getValorMensal() {
		return valorMensal;
	}
	public void setValorMensal(double vlMensal) {
		this.valorMensal = vlMensal;
	}
	public double getValorAnual() {
		return valorAnual;
	}
	public void setValorAnual(double valorAnual) {
		this.valorAnual = valorAnual;
	}
	public String getBeneficios() {
		return beneficios;
	}
	public void setBeneficios(String beneficios) {
		this.beneficios = beneficios;
	}
	
	
	
	
}
